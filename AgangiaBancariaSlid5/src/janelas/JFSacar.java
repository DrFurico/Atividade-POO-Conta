    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
         if((!"".equals(jtContaSacar.getText())) && (!"".equals(jtValorSacar.getText()))){
             Conta consultar = Dados.getConta(Integer.parseInt(jtContaSacar.getText()));
             if(consultar != null){
                 if(consultar.sacar(Double.parseDouble(jtValorSacar.getText()))== true){
                     jtNome.setText(consultar.getNomeTitular());
                     jtNascimento.setText(consultar.getDataNascimento());
                     jtCpfFixo.setText(consultar.getCpf());
                     jtValorConfirma.setText(Double.toString(consultar.getSaldo()));
                     
                 }else{
                     JOptionPane.showMessageDialog(null, "Saldo insuficiente","Erro",JOptionPane.INFORMATION_MESSAGE);
                 }
             }else{
                 JOptionPane.showMessageDialog(null, "Conta n√£o econtrada","Erro",JOptionPane.INFORMATION_MESSAGE);
             }
         }else {
             JOptionPane.showMessageDialog(null, "Informe todos os campos","Erro",JOptionPane.INFORMATION_MESSAGE);
         }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

