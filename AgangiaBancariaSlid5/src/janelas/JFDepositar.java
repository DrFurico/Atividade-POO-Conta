    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(!"".equals(jtConta.getText()) && !"".equals(jtValor.getText())){
             
            Conta consultar = Dados.getConta(Integer.parseInt(jtConta.getText()));
                if(consultar!= null){
                    
                    if(consultar.depositar(Double.parseDouble(jtValor.getText())) == true){
                 
                        JOptionPane.showMessageDialog(null, "Operação realizado com sucesso");
             
                    }else{
                        JOptionPane.showMessageDialog(null, "Valor invalido para o deposito","Erro",JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Conta não econtrada", "Erro!",JOptionPane.ERROR_MESSAGE);
                }
        } else{
            JOptionPane.showMessageDialog(null, "Informe todos os campos","Erro",JOptionPane.ERROR_MESSAGE );
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

