    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
       
       
       if(!"".equals(jtConta.getText()) || !"".equals(jtValor.getText())){
             Conta consultar = Dados.getConta(Integer.parseInt(jtConta.getText()));   
             
                    if(consultar != null){
                    jtNome.setText(consultar.getNomeTitular());
                    jtNascimento.setText(consultar.getDataNascimento());
                    jtCpfFixo.setText(consultar.getCpf());
                    jtValorConfirma.setText(jtValor.getText());
           
                } else {
                     JOptionPane.showMessageDialog(null, "Conta não econtrada", "Erro!",JOptionPane.ERROR_MESSAGE);
        
                    }
       }else{
            JOptionPane.showMessageDialog(null, "Informe todos os campos" );
        }  
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

